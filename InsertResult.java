    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String em = jTextField2.getText();
        String dbms = jTextField3.getText();
        String os = jTextField4.getText();
        String ml = jTextField5.getText();
        String maths = jTextField6.getText();
         java.sql.Connection con;
    java.sql.PreparedStatement pre;
    
        
        try{
        try{
        Class.forName("com.mysql.cj.jdbc.Driver");
       }catch(ClassNotFoundException ex){
                Logger.getLogger(CreateStudent.class.getName()).log(Level.SEVERE,null,ex);
            }
        con = (java.sql.Connection)DriverManager.getConnection("jdbc:mysql://localhost/srmportal","root","ramya@963@shetty");
        
        java.sql.Statement s;
            s = con.createStatement();
             ResultSet rs = s.executeQuery("Select *  from student where rollno='"+rollno+"'");
        if(rs.next())
        {
          pre = con.prepareStatement("Insert into result(rollno,em,dbms,os,ml,maths)values(?,?,?,?,?,?)");
         
        pre.setString(1,rollno);
        pre.setString(2,em);
        pre.setString(3,dbms);
        pre.setString(4,os);
        pre.setString(5,ml);
        pre.setString(6,maths);
        
        pre.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data saved successfully");
        setVisible(false);
        new AdminIndex().setVisible(true);  
        }
        else JOptionPane.showMessageDialog(null, "Roll number does not exist!"); 
        
        } catch (SQLException ex) {
            Logger.getLogger(InsertResult.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

